alert("Exercício 34 - Atividade De Respostas")

let x, y; 



y=parseInt(prompt("Digite um número para Y: "));
x=parseInt(prompt("Digite um número para X: "));

z=(x*y) + 5 

alert("A resposta de " + x + " + " + y + " é: " + z);

if (z<0) {
    alert ("A Resposta é A");
}
else if (z < 101) {
    alert ("A Resposta é B");
}
else {
    alert ("A Resposta é C");
}

